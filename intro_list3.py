# copy

fruits = ["mango", "chickoo", "apple", "banana", "grapes", "apple"]

print(fruits.count("apple")) # If you want to count how many time apples is there in the list

fruits.sort() # This will sort the items in list in alphabaticall order.
print(fruits)

print(sorted(fruits)) # The sorted function will sort the item while printing. It will not change in the list.

fruits.reverse() # This will print reverse alphabatic order from z to a
print(fruits)


fruits_copy = fruits.copy() # To copy list
print(fruits_copy)


fruits.clear() # This will clear the list item
print(fruits)



